import torch.nn as nn


class CNN(nn.Module):
    def __init__(self):
        super(CNN, self).__init__()

        # ── 특징 추출부 ──────────────────────────────────────────────
        # 이미지에서 "어떤 패턴이 있는지"를 찾아내는 부분
        # Conv2d가 이미지를 작은 필터로 훑으면서 엣지, 곡선 등 특징을 추출함
        self.features = nn.Sequential(

            # [Conv 1] 흑백 이미지(1채널) → 32가지 특징 맵
            # kernel_size=3: 3×3 필터로 이미지를 훑음
            # padding=1: 출력 크기를 입력과 동일하게 유지 (28×28 → 28×28)
            nn.Conv2d(1, 32, kernel_size=3, padding=1),
            nn.ReLU(),       # 음수를 0으로 만들어 비선형성 추가 (없으면 단순 선형 연산과 다를 바 없음)
            nn.MaxPool2d(2), # 2×2 영역에서 최댓값만 남김 → 28×28 → 14×14 (크기 절반, 중요한 특징만 유지)

            # [Conv 2] 32채널 → 64가지 특징 맵 (더 복잡한 패턴 학습)
            # 이 시점의 입력은 이미 Conv1이 찾은 저수준 특징들 (엣지 등)
            # Conv2는 그것들을 조합해 더 고수준 특징 (숫자의 곡선, 꺾임 등)을 학습
            nn.Conv2d(32, 64, kernel_size=3, padding=1),
            nn.ReLU(),
            nn.MaxPool2d(2), # 14×14 → 7×7
        )

        # ── 분류부 ───────────────────────────────────────────────────
        # 추출된 특징을 바탕으로 "이 이미지가 0~9 중 무엇인가"를 판단하는 부분
        self.classifier = nn.Sequential(

            # Flatten: 64채널 × 7×7 크기의 3D 특징 맵을 1D 벡터(3136개)로 펼침
            # Linear 레이어에 입력하려면 1D여야 하기 때문
            nn.Flatten(),

            # Linear: 완전연결층 (3136개 입력 → 128개 뉴런)
            # 모든 특징을 종합해 128차원의 표현으로 압축
            nn.Linear(64 * 7 * 7, 128),
            nn.ReLU(),

            # Dropout: 학습 중 30% 뉴런을 무작위로 꺼서 과적합 방지
            # 특정 뉴런에만 의존하지 않고 전체적으로 고르게 학습하게 함
            nn.Dropout(0.3),

            # 최종 출력: 128개 → 10개 (0~9 각 숫자일 확률)
            # 가장 큰 값의 인덱스가 모델의 예측 숫자
            nn.Linear(128, 10),
        )

    def forward(self, x):
        # 순전파: 입력 이미지 x가 특징 추출 → 분류 순으로 통과
        x = self.features(x)      # (배치, 1, 28, 28) → (배치, 64, 7, 7)
        x = self.classifier(x)    # (배치, 64, 7, 7) → (배치, 10)
        return x                  # 10개 클래스의 점수(logit) 반환
